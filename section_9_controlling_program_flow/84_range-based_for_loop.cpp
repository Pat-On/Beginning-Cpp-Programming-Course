/*

        #84 Range Based for Loop


            introduced in C++ 11

                for (var_type var_name: sequence)
                    statement;   can use var_name

                for (var_type var_name: sequence) {
                    statements; // can use var_name
                }

    ---------------------------------------------------------
        example:

        int scores [] {100, 90, 97};

        for (int score: scores)
            cout << score << endl;;


        auto type

        for (auto score: scores)
            cout << score << endl;


*/